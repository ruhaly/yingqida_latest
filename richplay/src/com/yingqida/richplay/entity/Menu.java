package com.yingqida.richplay.entity;

public class Menu {

	// 菜单名字
	private String name;

	// 菜单图标(可扩展)
	private int rid;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRid() {
		return rid;
	}

	public void setRid(int rid) {
		this.rid = rid;
	}

}
